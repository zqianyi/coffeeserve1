package com.city.coffeeserve.note.value;

public class FoodValue {
	private int fid;
	private String fname;
	private String ftype;
	private String fprice;
	private String fshock;
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFtype() {
		return ftype;
	}
	public void setFtype(String ftype) {
		this.ftype = ftype;
	}
	public String getFprice() {
		return fprice;
	}
	public void setFprice(String fprice) {
		this.fprice = fprice;
	}
	public String getFshock() {
		return fshock;
	}
	public void setFshock(String fshock) {
		this.fshock = fshock;
	}
	
}
